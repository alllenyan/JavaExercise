public class DataType {
    public static void main(String[] args) {
        //数据类型

        //整型
        byte b = -128;
        short s = 50;
        int i = 10;//Java默认的整数类型
        long l = 76;

        //浮点型
        float f = 1.22f;
        double d = 4.55;//Java默认的浮点类型

        //字符型
        char c = 'M';

        //布尔类型
        boolean b1 = true;


    }
}
