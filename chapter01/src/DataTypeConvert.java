public class DataTypeConvert {
    public static void main(String[] args) {

        //强制类型转换，float --> int
        float f = 1.22f;
        int i = (int)f;
        System.out.println(i);

        //强制类型转换，char --> int
        char c = 'a';
        int i1 = (int)c;
        System.out.println(i1);

    }
}
